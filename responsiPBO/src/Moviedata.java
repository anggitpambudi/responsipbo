    private void datatable() {
        DefaultTableModel tb1 = new DefaultTableModel();
        tb1.addColumn("Judul");
        tb1.addColumn("Alur");
        tb1.addColumn("Penokohan");
        tb1.addColumn("Akting");
        tb1.addColumn("Nilai");
        isitable.setModel(tb1);
        try{
            statement = (Statement) konek.getConnection().createStatement();
            ResultSet res;
            res = statement.executeQuery("Select * from movie");
            while (res.next())
            {
                tb1.addRow(new Object []{
                    res.getString("judul"),
                    res.getString("alur"),
                    res.getString("penokohan"),
                    res.getString("akting"),
                    res.getString("nilai"),
                });
                isitable.setModel(tb1);
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, "Gagal");
        }
    }
