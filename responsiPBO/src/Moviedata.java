    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        if(btupdate.getText() == "btupdate"){
           txtjudul.requestFocus();
        }else{
            int akting = parseInt(txtakting.getText());
            int alur = parseInt(txtalur.getText());
            String judul = txtjudul.getText();
            int penokohan = parseInt(txtpenokohan.getText());
            int nilai = (alur+akting+penokohan)/3;
            
        if(alur > 5.0 || penokohan > 5.0 || akting > 5.0){
               JOptionPane.showMessageDialog(null,"Maksimal alur, penokohan, dan akting adalah 5!"); 
            } else if(alur < 0 || penokohan < 0 || akting < 0){
                JOptionPane.showMessageDialog(null,"Minimal alur, penokohan, dan akting adalah 0!");
            } else{
                    try{
                        Statement statement = (Statement) konek.getConnection().createStatement();
                        statement.executeUpdate("UPDATE `movie` SET `judul` = '"+txtjudul.getText()+"', `alur` = '"+txtalur.getText()+"', `penokohan` = '"+txtpenokohan.getText()+"'"
                                + ", `akting` = '"+txtakting.getText()+"', `nilai` = '"+nilai+"' WHERE `movie`.`judul` = '"+judul+"';");
                        statement.close();
                        JOptionPane.showMessageDialog(null, "Data Berhasil diubah");
                        }catch (Exception t){
                        JOptionPane.showMessageDialog(null, "Data Gagal diubah");
                        }
                    }
        datatable();
        }
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
            String akting = txtakting.getText();
            String alur = txtalur.getText();
            String judul = txtjudul.getText();
            String penokohan = txtpenokohan.getText();
            String nilai = String.valueOf((Integer.parseInt(alur)+Integer.parseInt(akting)+Integer.parseInt(penokohan))/3);
        try{
            Statement statement = (Statement) konek.getConnection().createStatement();
            statement.executeUpdate("delete from movie where judul = '" +judul+ "';");
            JOptionPane.showMessageDialog(null, "Data Berhasil di Hapus");
            txtjudul.setText("");
            txtalur.setText("");
            txtpenokohan.setText("");
            txtakting.setText("");
            txtjudul.requestFocus();
        }catch (Exception t){
            JOptionPane.showMessageDialog(null, "Data Gagal di Hapus");
        }
        datatable();
    }//GEN-LAST:event_btndeleteActionPerformed

