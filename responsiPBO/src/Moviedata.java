    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        // TODO add your handling code here:
        int akting = parseInt(txtakting.getText());
        int alur = parseInt(txtalur.getText());
        String judul = txtjudul.getText();
        int penokohan = parseInt(txtpenokohan.getText());
        int nilai = (alur+akting+penokohan)/3;
        
        if(alur > 5 || penokohan > 5 || akting > 5){
               JOptionPane.showMessageDialog(null,"Maksimal alur, penokohan, dan akting adalah 5!"); 
            } else if(alur < 0 || penokohan < 0 || akting < 0){
                JOptionPane.showMessageDialog(null,"Minimal alur, penokohan, dan akting adalah 0!");
            } else{
                try{
                Statement statement = (Statement) konek.getConnection().createStatement();
                statement.executeUpdate("INSERT INTO `movie` (`judul`, `alur`, `penokohan`, `akting`, `nilai`) VALUES ('" + txtjudul.getText() + "', '" + txtalur.getText() + "', '" + txtpenokohan.getText() + "', '" + txtakting.getText() + "', '"+ nilai + "')");
                statement.close();
                JOptionPane.showMessageDialog(null, "Data Berhasil disimpan");
                }catch (Exception t){
                    JOptionPane.showMessageDialog(null, "Data sudah ada");
                }
            }
    datatable();  
    }//GEN-LAST:event_btntambahActionPerformed

